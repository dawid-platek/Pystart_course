a = int(input('Enter the length of the first side of the trapezoid: '))
b = int(input('Enter the length of the second side of the trapezoid: '))
h = int(input('Enter the height of the trapezoid: '))
trapezoid_area = ((a + b) / 2) * h  # Calculate the area of the trapezoid
print(f'The area of the trapezoid is: {trapezoid_area:.2f}')  # Print the area rounded to two decimal places

# This code calculates the area of a trapezoid given the lengths of its two parallel sides and its height.